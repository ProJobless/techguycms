<p>Modules are blocks of code and/or content around the website. Module locations depend on your website template/design. Most templates have either
a left or right sidebar position.</p>
<p>You can manage modules from the dashboard quick links or from the main navigation. 
You can add, edit and delete modules BUT I would not suggest changing anything unless needed and you know what you are doing</p>
<img src="<?php echo base_url(); ?>assets/images/admin/mod_list.jpg" />
<h3>Editable Modules</h3>
<p>If you go to the module manager in the admin panel, you will see that some are "Editable" and some are not. If the module is a straight html/content module,
it is editable and you can change the content of the module in the admin area. If it is not editable, this means that it is a module that involves programming to change anything.
This would be a module such as a search or another type of dynamic module. To edit these, you would need to edit the code in the view files.</p>
<img src="<?php echo base_url(); ?>assets/images/admin/mod_edit.jpg" />
<h3>Global Modules</h3>
<p>If a module is global, it will show on every single page of the website. If not, then it will only show on the pages and posts that you choose.
To change which pages/posts that each module shows up on, can be edited in the page manager and/or the post manager.</p>
<h3>Class Suffixes</h3>
<p>You can specify a class suffix for each module which will allow you to style each one differently. You must have some knowledge of CSS to do this</p>


