<p>Settings are accessible from the admin area. Below are some of the options available</p>
<ul>
    <li>Website Name - This will be the text that will be displayed if you choose to use a text logo</li>
    <li>Headline Text - This will be displayed below the website name in a text logo</li>
    <li>Logo Type - You can choose to use an image as a logo or use the site name & headline</li>
    <li>Logo - Upload field for logo</li>
    <li>Copyright - This will show at the bottom of your site</li>
    <li>SEO Options - You can add/edit seo options such as global page title, keywords and description</li>
    <li>Google Analytics - You can paste in your Google Analytics code to track traffic</li>
</ul>

