<h3>User Roles/Groups</h3>
<p>There are 3 user roles which all have certain permissions. Below are the roles available</p>
<ul>
    <li>Registered - Registered users will have front end access only. If a user registers in the front end of the website, they will be placed in the "Registered" role</li>
    <li>Author - Authors can create posts in the admin panel but can not create pages or do any other type of admin tasks</li>
    <li>Admin - Admins have access to all back end admin panel features such as page, post, user and module management</li>
</ul>
<h3>User Activation</h3>
<p>When a user registers in the front end, they will get a confirmation email from Techguy CMS in which they need to click on the link to become
an activated member. Once activated, they can log in. Admins can also activate users from the users back end profile</p>


