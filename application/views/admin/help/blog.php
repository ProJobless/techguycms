<h4>Fields & Options</h4>
<ul>
    <li>Post Title - The title of the post</li>
    <li>Post Body - The main content in your post. The body uses a Microsoft Word-Like editor which makes it easy to change font styles, add images to posts and more</li>
    <li>Page Modules - This is where you can select which modules you want to display on that current page. Some modules are "global" and those
    will show on the page regardless of what you choose here.</li>
    <li>Category - Allows you to categorize posts. If no category is chosen, it will be posted as "Uncategorized"</li>
    <li>Author - By default your name should be selected. You can change it to another user with blogging privileges</li>
    <li>Page Modules - This is where you can select which modules you want to display on that current post. Some modules are "global" and those
    will show on the post page regardless of what you choose here.</li>
    <li>Published - Publish and unpublish posts</li>
    <li>Main Image - You can add images to any post through the editor. This image is the main image that will display on the list of posts.
    It is set to a fixed size.</li>
    <li>Page Title - This is different than the "Page Name". The page name is just the heading of the page. The "Page Title" 
        is a meta field used for SEO. It will also show in the browsers tab. You can choose a "global" page title in the settings area
    and leave this blank if preferred</li>
    <li>Meta Keywords/Tags & Description - These are also for SEO. Again, you can leave these blank and use the "global" SEO settings. The meta keywords and tags are the same thing. Tags are used for on site searching and meta key words are more for global SEO</li>
    <li>Menu Options - This is important. If you do not choose a menu, the page will not be accessible on your site. Choose a menu, give it a title and choose the order position of the menu item</li>
    
</ul>
<h3>Categories</h3>
<p>The TGCMS blog functionality allows you to create categories and link posts to a specific category. You can easily add categories
by clicking the "Add Category" link on the post listing page.</p>

<h3>Comments</h3>
<p>If enabled, users can leave comments on your blog posts. You can control how they work from the settings area. There is a comments interface
in the admin area where you can view all comments and approve or not approve each one</p>
