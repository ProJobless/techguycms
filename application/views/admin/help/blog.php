<h3>Adding & Editing Blog Posts</h3>
<p>Adding and managing posts is almost exact to managing pages. The main content uses a wysiwyg editor for editing and adding posts. This allows
you to manage content using a Microsoft Word-Like interface. You can add text, images and videos through the editor.</p>
